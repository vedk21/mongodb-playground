/*
  In mongoDB we can have controlled access to the database server using authentication and authorization techniques.

  Authentication is a process of giving access to a database server to a user.
  Authorization is a process of limiting the access of a user inside the database server.

  There are different sets of privileges a user can have and if we group these privileges its called roles. We can assign different roles to different users in the database system.
  The privilege is a combination of resource and access.
  Example:
  Resource -> Shop DB => Products Collection
  Access -> find(), update(), insert()
*/